export function getIcon(type, rarity) {
  switch (type) {
    case "head":
      return HELMET[rarity - 1];
    case "chest":
      return CHEST[rarity - 1];
    case "gloves":
      return GLOVES[rarity - 1];
    case "waist":
      return WAIST[rarity - 1];
    case "legs":
      return LEGS[rarity - 1];
    case "charm":
      return TALISMAN[rarity - 1];
    default:
      return "";
  }
}

